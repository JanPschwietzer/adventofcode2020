# The important thing is that the 31 digits show us the universal pattern of that row.
# Everytime I went out of range I reset (g) to an indexnumber that is not out of range. 
# 
#       in range                  out of range, gets reset
#          v                          v
#     __________> g      __________> g
#    |                  |
#    |                  |
#    |                  |
#    v                  v
#    i                  i

