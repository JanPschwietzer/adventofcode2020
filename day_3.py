### global file-opener ###

file = open("list", "r")
list = file.read().split("\n")

class Calc:

    g = 0
    
# We have to count every "#" that is g+3 and i+1 from out last position
# If g is out of range we set it back to the right position with the Calc.g_counter() function
#
    def count_3_1():
        c_t = 0
        Calc.g = 0
        for i in range(len(list)):
            Calc.g = Calc.g + 3
            Calc.g_counter()
            i = i + 1
            if i == 323: # important because the for loop will go out of range without!
                break
            if list[i][Calc.g] == "#":
                c_t += 1
        return c_t

